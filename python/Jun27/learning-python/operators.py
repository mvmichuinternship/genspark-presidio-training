#Comparison operators
e=5
f=6
print(e==f)
print(e!=f)
print(e>f)
print(e<f)
print(e>=f)
print(e<=f)

#Logical operators
# AND OR NOT

g=5 
h=6
print(g>4 and h<7)
print(g>5 and h<6)
print(g>5 or h<7)
print(g>4 or h<7)
print(g>4 or h<6)
print(not(g>4 ))
print(not(g>5 ))

#Bitwise operators
#Converts to binary and compares corresponding binary digits
print(6 & 3)
print(6 | 3)
print(6 ^ 3)
print(~6)

