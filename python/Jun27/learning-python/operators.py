#Arithmetic operators
#Addition, Subtraction, Multiplication, Division, Modulo, Exponentitation, Floor Division
a=10
b=4
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)
print(a//b)

